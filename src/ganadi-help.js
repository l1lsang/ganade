export function buildGanadiHelpEmbed(category = 'start') {
  const selected = helpCategories[category] || helpCategories.start;

  return new EmbedBuilder()
    .setColor(0xffb7d5)
    .setTitle(`${selected.emoji} ${selected.title}`)
    .setDescription(selected.description)
    .addFields(selected.fields)
    .setFooter({ text: '일반 멤버용 도움말 · 필수 옵션은 < >, 선택 옵션은 [ ]로 표시했어!' });
}
